function renderColor() {
        
    // Element cha
    const parentElement = document.querySelector('#colorList');

    // Chia mảng thành các nhóm con (mỗi nhóm có 6 phần tử)
    const chunkedColors = [];
    const chunkSize = 6;
    for (let i = 0; i < colors.length; i += chunkSize) {
        chunkedColors.push(colors.slice(i, i + chunkSize));
    }

    chunkedColors.forEach((group, groupIndex) => {
        // Tạo phần tử div (cụm)
        const groupElement = document.createElement('div');
        groupElement.className = 'row mt-2';

        group.forEach(color => {
            // Tạo phần tử div (một màu)
            const colorElement = document.createElement('div');
            colorElement.className = 'col-2 item';

            // Tạo phần tử div chứa màu
            const colorBoxElement = document.createElement('div');
            colorBoxElement.className = 'colorBg scale-box';
            colorBoxElement.style.backgroundColor = color.mau;
            colorBoxElement.setAttribute('data-code', color.maMau);
            colorBoxElement.setAttribute('data-name', color.tenMau);

            // Tạo phần tử h4
            const h4Element = document.createElement('h4');
            h4Element.className = 'color-title';
            h4Element.innerHTML = color.tenMau;

            // Tạo phần tử span (mã màu)
            const codeElement = document.createElement('span');
            codeElement.className = 'code';
            codeElement.innerHTML = color.maMau;

            // Tạo phần tử span (mũi tên)
            const arrowElement = document.createElement('span');
            arrowElement.className = 'arrow';
            arrowElement.style.backgroundColor = 'rgb(236, 224, 202)';

            // Gắn các phần tử con vào phần tử chính
            colorBoxElement.appendChild(h4Element);
            colorBoxElement.appendChild(codeElement);
            colorBoxElement.appendChild(arrowElement);
            colorElement.appendChild(colorBoxElement);

            // Thêm phần tử vào cụm
            groupElement.appendChild(colorElement);
            
        });
        const separatorElement = document.createElement('div');
        separatorElement.className = 'col-12 item mt-2';
        separatorElement.id = `row${groupIndex}`;
        groupElement.appendChild(separatorElement);

        // Thêm cụm vào element cha
        parentElement.appendChild(groupElement);
    });






    const a = document.querySelectorAll(".colorBg");
    a.forEach((item,index) => {
        const indexItem=Math.floor(index/6)
    item.addEventListener("click", (e) => {
        renderPicture(e.target.attributes.style.nodeValue,e.target.dataset.name,indexItem);

    });
    });

    const renderPicture = (color,name,index) => {
        console.log(index);
        if(document.getElementById(`row${index}`).childElementCount!=0)
        {
            document.getElementById(`row${index}`).innerText=''
        }
        document.getElementById(`row${index}`).innerHTML += `
        <div class="d-flex justify-content-between palette" >
            <div class="picture " style='background-image: url(./assets/img/hinoColor/ddxe01.png);${color}'>
            </div>
            
            <div class="picColor" style='${color}'>
                <h4>${name}</h4>
            </div>
        </div>
        `;
    };

    const clearPicture = () => {
        console.log(document.querySelectorAll('.palette'))
    }

}

